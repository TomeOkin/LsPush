package com.tomeokin.lspush.data.model;

public class SMSCheckCaptchaResponse extends BaseResponse {
    private String phone;
    private String countryCode;

    public SMSCheckCaptchaResponse(String phone, String countryCode) {
        this.phone = phone;
        this.countryCode = countryCode;
    }

    public SMSCheckCaptchaResponse(int resultCode, String result, String phone, String countryCode) {
        super(resultCode, result);
        this.phone = phone;
        this.countryCode = countryCode;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }
}
