package com.tomeokin.lspush.data.model;

import java.util.List;

public class CollectionResponse extends BaseResponse {
    private List<Collection> collections;

    public CollectionResponse(List<Collection> collections) {
        super();
        this.collections = collections;
    }

    public CollectionResponse(int resultCode, String result, List<Collection> collections) {
        super(resultCode, result);
        this.collections = collections;
    }

    public List<Collection> getCollections() {
        return collections;
    }

    public void setCollections(List<Collection> collections) {
        this.collections = collections;
    }
}
